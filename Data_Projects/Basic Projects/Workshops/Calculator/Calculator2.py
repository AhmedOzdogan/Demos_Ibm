# -*- coding: utf-8 -*-

class matematik:
    
    def __init__(self,num1,num2):
        self.num1 = num1
        self.num2 = num2
        
    def plus(self):
        return self.num1+self.num2
    
    def minus(self):
        return self.num1-self.num2
    
    def multi(self):
        return self.num1*self.num2
    
    def division(self):
        return self.num1/self.num2




